                    경매서비스는 당사가 회원 각자의 자기결정에 의하여 물품매매거래가 이루어질 수 있도록 사이버 거래장소를 온라인으로 제공하는 것입니다.<br>
                    <br>
                    제6조 [서비스 이용계약]<br><br>
                    가. 당사가 제공하는 서비스에 관한 이용계약(이하 “이용계약”이라고 합니다)은 서비스를 이용 하고자 하는 자의 이용신청(회원가입신청)에 대하여 당사가 승낙함으로써 성립합니다. 당사 는 이용승낙의 의사를 해당 서비스 화면에 게시하거나, 이메일 또는 기타 방법으로 이용신 청자에게 통지합니다.<br>
                    나. 회원가입은 개인 또는 사업자가 할 수 있습니다. 이용신청자는 실명으로 가입신청을 해야 하며, 실명이 아니거나 타인의 정보를 도용하여 회원으로 가입하는 자는 당사가 제공하는 서비스를 이용할 수 없으며, 이용하여서도 안됩니다.<br>
                    다. 미성년자가 회원가입을 하고자 할 경우 법정대리인의 동의를 받은 후 가입하여야 합니다.<br>
                    라. 아이디(ID)는 실명 1인당 1개만을 사용할 수 있으며, 이용계약은 아이디(ID) 단위로 체결하 는 것을 원칙으로 합니다.<br>
                    <br>
                    제7조 [이용신청]<br><br>
                    가. 서비스를 이용하고자 하는 자는 아래 사항을 당사가 온라인으로 제공하는 가입신청양식에 따라 기재하여야 합니다.
                    ①성명 ②주민등록번호 또는 휴대폰 인증 ③주소 ④전화번호 ⑤이메일 주소 ⑥아이디(ID) ⑦비밀번호(PASSWORD) ⑧성별 ⑨관심분야 ⑩직업<br>
                    나. 법인은 사업자회원으로 가입 신청하여야 하고, 개인사업자의 경우에도 그 사업과 관련하여 서비스를 이용하고자 하는 때에는 사업자회원으로 가입하여야 합니다. 사업자회원으로 가입 하고자 하는 자는 당사의 요청에 따라 사업자등록증 등의 증빙자료와 추가 정보를 제공하여 야 합니다.<br>
                    다. 당사는 위 항의 필수 기재항목 이외에도 회원에게 다양하고 유익한 정보를 제공하기 위하여 이용신청자에게 직업에 대한 추가 사항, 취미, 관심분야 등의 정보를 제공할 것을 요청할 수 있습니다. 다만, 이용신청자는 그러한 추가 정보의 제공을 거절할 수 있습니다.<br>
                    <br>
                    제8조 [이용신청의 승낙]<br><br>
                    가. 당사는 이용신청자가 제7조에서 정한 필수기재항목을 정확하게 기재하고 이 약관에 동의한 경우에는 이 약관에 따른 실명확인절차를 거쳐 서비스의 이용을 승낙하는 것을 원칙으로 합 니다.<br>
                    나. 당사는 개인회원가입신청의 경우 법령에 따라 이용 가능한 신용정보업자 또는 신용정보집중 기관을 통하여 주민등록번호 혹은 휴대폰 인증, 성명의 일치 여부를 확인함으로써 실명확인 절차를 취하며, 사업자회원가입신청의 경우에는 제7조 나항에 따라 제공받는 증빙자료 등에 의하여 실명확인절차를 취합니다. 당사는 실명확인절차를 취할 수 없는 이용신청에 대해서 는 이용 신청자에게 추가적인 증빙자료를 요청할 수 있습니다.<br>
                    다. 당사는 다음과 같은 사유가 있는 경우, 이용신청에 대한 승낙을 거부할 수 있습니다.<br>
                    1) 미성년자가 법정대리인의 동의 없이 이용신청을 하는 경우<br>
                    2) 당사의 실명확인절차에서 실명가입신청이 아님이 확인된 경우<br>
                    3) 이미 가입된 회원과 이름 및 주민등록번호(또는 사업자등록번호)가 동일한 경우<br>
                    4) 이 약관 제9조에 의하여 당사가 이용계약을 해지한 전(前) 회원이 재이용신청을 하는 경우<br>
                    5) 이 약관 제14조에 의하여 당사로부터 회원자격정지조치를 받은 회원이 이용정지기간 중 에 이용계약을 임의 해지하고 재이용신청을 하는 경우<br>
                    6) 가입신청 시 기재해야 할 필수기재항목에 허위사실을 기재한 경우<br>
                    7) 기타 위법 또는 부당한 이용신청임이 확인된 경우<br>
                    라. 당사는 다음과 같은 사유가 있는 경우, 이용신청에 대한 승낙을 유보할 수 있습니다. 이 경 우 당사는 이용신청자에게 승낙유보의 사유, 승낙가능시기 또는 승낙에 필요한 추가요청 정 보 내지 자료 등 기타 승낙유보와 관련된 사항을 해당 서비스 화면에 게시하거나 이메일을 통하여 통지합니다.<br>
                    1) 서비스 제공과 관련된 당사 설비에 추가 신청을 받아들일 여유가 없는 경우<br>
                    2) 기술상 지장이나 장애가 있는 경우<br>
                    3) 당사의 실명확인절차에서 실명가입여부가 확인되지 않는 경우<br>
                    4) 사업자회원가입을 신청한 자가 제7조 나항의 증빙자료 또는 추가 정보를 제공하지 않는 경우<br>
                    5) 기타 당사가 합리적인 판단에 의하여 필요하다고 인정하는 경우<br>
                    <br>
                    제9조 [이용계약의 종료]<br><br>
                    가. 회원 또는 당사는 이 약관에서 정한 절차에 따라 이용계약을 해지할 수 있습니다.<br>
                    나. 회원의 해지<br>
                    1) 회원은 언제든지 해지의사를 통보함으로써 이용계약을 해지할 수 있습니다. 다만, 회원 은 해지의사를 통지하기 전에 모든 진행중인 경매절차를 완료, 철회, 취소하여 이를 종 료하여야만 합니다. 이 경우 진행 중인 경매절차가 적절하게 종료되지 아니하면 회원해 지를 하실 수 없습니다.<br>
                    2) 이용계약은 회원의 해지의사가 당사에 도달한 때에 종료됩니다.<br>
                    3) 본 항에 따라 해지를 한 회원은 이 약관이 정하는 회원가입절차와 관련 조항에 따라 회 원으로 다시 가입할 수 있습니다.<br>
                    다. 당사의 해지<br>
                    1) 당사는 다음과 같은 사유가 있는 경우, 이용계약을 해지할 수 있습니다. 이 경우 당사는 회원에게 이메일, 전화 또는 팩스 기타의 방법을 통하여 해지사유를 밝혀 해지의사를 통 지합니다. 다만, 당사는 해당 회원에게 사전에 해지사유에 대한 의견진술의 기회를 부여 합니다.<br>
                    ① 회원에게 제8조 다항에서 정하고 있는 이용계약의 승낙거부사유가 있음이 확인된 경우<br>
                    ② 회원이 선량한 풍속이나 기타 사회질서에 위배되는 물품거래행위를 하거나 시도한 경우<br>
                    ③ 회원이 당사나 다른 회원, 기타 타인의 권리나 명예, 신용, 기타 정당한 이익을 침 해하는 행위를 한 경우<br>
                    ④ 회원이 직거래 등 당사가 제공하는 서비스의 원활한 진행을 방해하는 행위를 하거 나 시도한 경우<br>
                    ⑤ 회원이 실제로 물품을 판매하고자 하는 의사 없이 물품등록을 의뢰한 경우 또는 이를 알고 낙찰받은 것으로 인정되는 경우<br>
                    ⑥ 기타 회원이 이 약관에 위배되는 행위를 한 경우<br>
                    2) 이용계약은 당사의 해지의사가 회원에게 도달한 때에 종료되나, 회원이 제공한 연락처 정보의 오류로 인하여 당사가 해지의사를 통보할 수 없는 경우에는 당사가 My 옥션에 해지의사를 공지하는 때에 종료됩니다.<br>
                    3) 당사가 이용계약을 해지하는 경우, 당사는 별도의 통지 없이 해당회원과 관련된 경매절 차를 취소 또는 강제적으로 종료할 수 있습니다.<br>
                    라. 이용계약의 종료와 관련하여 회원에게 발생하는 손해는 이용계약이 종료된 해당회원이 책임 을 부담하여야 하고, 그러한 손해가 당사의 귀책사유에 근거한 것이 아닌 한 당사는 일체의 책임을 지지 않습니다.<br><br>

