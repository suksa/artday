                    제4조 [관련법령과의 관계]<br><br>
                    가. 이 약관 또는 개별약관에서 정하지 않은 사항은 전기통신사업법, 전자거래기본법, 정보통신 망 이용촉진 및 정보보호 등에 관한 법률, 개인정보보호법 등 관련 법령의 규정과 일반적인 상관례에 의합니다.<br>
                    나. 회원은 당사가 제공하는 서비스를 이용함에 있어서 방문판매 등에 관한 법률, 전자거래기본 법, 소비자기본법, 표시ㆍ광고의 공정화에 관한 법률, 정보통신망 이용촉진 및 정보보호 등 에 관한 법률 등 관련법령을 준수하여야 하며, 이 약관의 규정을 들어 관련 법령 위반에 대 한 면책을 주장할 수 없습니다.<br>
