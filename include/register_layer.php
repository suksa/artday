                    3) 비밀번호(PASSWORD): 회원의 동일성 확인과 회원정보의 보호를 위하여 회원이 설정하 고 당사가 승인한 문자와 숫자의 조합을 말합니다.<br>
                    4) 위탁자: 물품을 판매할 의사로 당사가 온라인으로 제공하는 양식에 맞추어 해당 물품을 등록 한 회원을 말합니다.<br>
                    5) 낙찰자: 경매물품을 낙찰 받은 회원을 말합니다.<br>
                    6) 응찰: 경매물품을 구매하기 위하여 원하는 매수가격을 제출하는 행위를 말합니다.<br>
                    7) 낙찰: 응찰에 의하여 경매물품의 거래가 성립되는 것을 말합니다.<br>
                    8) 응찰단위: 경매응찰시 입력할 수 있는 최소 단위의 금액을 말합니다.<br>
                    9) 일반경매: 응찰자 중 최고가격 응찰자에게 낙찰이 이루어지는 경매방식을 말합니다.<br>
                    10) 즉시구매: 판매자가 표시한 즉시구매가격에 응찰함으로써 즉시 낙찰이 이루어지는 경매 방식을 말합니다.<br>
                    11) 이벤트 경매: 당사가 온라인을 통해 다양한 물품을 테마를 정해서 진행하는 경매를 말 합니다.<br>
                    12) My 옥션: 각 회원이 경매서비스를 신속하고 편리하게 이용하도록 하기 위하여 당사가 각 회원에 대한 판매정보 및 각 회원의 구매정보, 신용도, 회원정보 등 회원 의 경매 기록을 모아 제공하는 서비스 화면을 말합니다.<br>
                    13) 피연결회사: 당사의 서비스 화면과 링크 등으로 연결된 사이트를 운영하는 회사를 말합 니다.<br><br>
                    제3조 [약관의 명시, 효력 및 변경]<br><br>
                    가. 당사는 이 약관을 서비스를 이용하고자 하는 자와 회원이 알 수 있도록 서비스가 제공되는 인터넷 사이트(http://www.artday.co.kr) 화면에 게시합니다.<br>
