                    이 약관은 주식회사 헤럴드아트데이(이하 “당사”라고 합니다)가 인터넷 사이트(http://www.artday.co.kr)를 통하여 제공하는 온라인 경매서비스 및 기타 경매 관련 정보 등을 제공하는 서비스(이하 “서비스”라고 합니다)에 대한 당사와 회원간의 권리와 의무, 책임사항 및 회원의 서비스 이용절차에 관한 사항을 규정함을 목적으로 합니다.<br><br>

