            ① 회원에게 제8조 다항에서 정하고 있는 이용계약의 승낙거부사유가 있음이 확인된 경우<br>
            ② 회원이 선량한 풍속이나 기타 사회질서에 위배되는 물품거래행위를 하거나 시도한 경우<br>
            ③ 회원이 당사나 다른 회원, 기타 타인의 권리나 명예, 신용, 기타 정당한 이익을 침해하는 행위를 한 경우<br>
            ④ 회원이 직거래 등 당사가 제공하는 서비스의 원활한 진행을 방해하는 행위를 하거나 시도한 경우<br>
            ⑤ 회원이 실제로 물품을 판매하고자 하는 의사 없이 물품등록을 의뢰한 경우 또는 이를 알고 낙찰받은 것으로 인정되는 경우<br>
            ⑥ 기타 회원이 이 약관에 위배되는 행위를 한 경우<br>
            2) 이용계약은 당사의 해지의사가 회원에게 도달한 때에 종료되나, 회원이 제공한 연락처 정보의 오류로 인하여 당사가 해지의사를 통보할 수 없는 경우에는 당사가 임의로 회원의 해지를 진행합니다.<br>
            3) 당사가 이용계약을 해지하는 경우, 당사는 별도의 통지 없이 해당회원과 관련된 경매절차를 취소 또는 강제적으로 종료할 수 있습니다.<br>
            라. 이용계약의 종료와 관련하여 회원에게 발생하는 손해는 이용계약이 종료된 해당회원이 책임을 부담하여야 하고, 그러한 손해가 당사의 귀책사유에 근거한 것이 아닌 한 당사는 일체의 책임을 지지 않습니다.<br>
