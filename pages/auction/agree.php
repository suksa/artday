            2) 이용계약은 회원의 해지의사가 당사에 도달한 때에 종료됩니다.<br>
            3) 본 항에 따라 해지를 한 회원은 이 약관이 정하는 회원가입절차와 관련 조항에 따라 회원으로 다시 가입할 수 있습니다.<br>
