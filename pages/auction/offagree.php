            1. 목적<br><br>
            헤럴드아트데이 경매약관(이하 “이 약관”)은 주식회사 헤럴드아트데이(이하 “당사”)가 경매를 실시함에 있어 경매 절차 및 제반 기준과 당사와 이용자의 권리•의무에 대하여 규정함을 목적으로 합니다.<br>

            2. 용어의 정의<br><br>
            2.1	“경매”라 함은 당사가 이 약관의 절차에 따라 응찰자 중 낙찰자를 선정하여, 당사와 낙찰자 사이에 매매가 이루어지도록 하는 행위를 말합니다. <br>
            2.2	“경매물품”이라 함은 위탁자가 당사에게 경매의 방법으로 매도하여 줄 것을 위탁한 특정 물품을 말합니다. <br>
            2.3	“구매대금”이라 함은 낙찰가와 구매수수료를 합한 금액을 의미합니다. <br>
            2.4	“구매수수료”라 함은 낙찰자가 당사에 지급하여야 하는 수수료로, 이 약관 제8조에서 정한 바에 따라 산정됩니다. <br>
            2.5	“낙찰가”라 함은 경매물품에 대하여 응찰자가 제시한 입찰가격 중 내정가 이상의 가격으로서 최고 입찰가격을 의미합니다. 다만, 이 약관 제7.3조에 따라, 당사는 예외적으로 내정가 미만의 가격으로 낙찰가를 결정할 수 있습니다.<br>
            2.6 “낙찰자”라 함은 응찰자 중 낙찰가를 제시하여 경매물품을 낙찰 받은 개인 또는 법인을 말합니다.<br>
            2.7	“내정가”라 함은 당사와 위탁자의 합의에 의하여 결정되며, 위탁자 이외의 자에게 공개되지 않는, 경매물품에 대한 낙찰 가능한 최저 가격을 말합니다. 다만, 이 약관 제7.3조에 따라, 당사는 예외적으로 내정가 미만의 가격으로 낙찰가를 결정할 수 있습니다.    <br>
            2.8	“위탁자”라 함은 자기 소유의 경매물품을 당사경매에 출품하기 위하여 당사와 위탁계약을 체결하고 이를 위탁한 개인 또는 법인을 말합니다. <br>
            2.9	“응찰자”라 함은 당사의 경매에 참가하여 경매물품을 낙찰 받고자 하는 개인 또는 법인을 말합니다.<br>
            2.10	“이용자”라 함은 당사의 경매에 참가하거나 당사의 정보 또는 서비스를 제공받는 위탁자, 응찰자, 낙찰자 및 회원을 말합니다.<br>
            2.11	“추정가”라 함은 당사가 당사의 판단에 따라 경매물품에 대하여 예상되는 낮은 추정가와 높은 추정가를 표시한 가격을 의미합니다. 추정가는 단순히 응찰자의 편의를 위하여 정보 제공의 목적으로 기재되는 것이며, 낙찰가와 관련이 없습니다. 당사는 추정가의 결정 및 기재와 관련하여 이용자에게 어떠한 책임도 부담하지 않습니다. <br>
            2.12	 “경매도록”이라 함은 당사경매와 관련하여 헤럴드아트데이가 발간하는 당사 경매에서 경매될 예정인 경매물품에 관한 설명과 정보가 실린 도록을 의미합니다.<br>
            2.13	 “경매일”이라 함은 경매물품에 관하여 경매물품이 당사경매에서 경매에 부쳐지는 일자를 의미합니다.<br>
