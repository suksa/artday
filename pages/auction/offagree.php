            7. 경매방법 <br><br>
                7.1 경매는 경매도록에 기재되어있는 Lot 순서에 따라 차례대로 진행됩니다.<br>
            7.2 헤럴드아트데이는 위탁자와 합의한 내정가를 지키기 위하여 내정가에 도달할 때까지 응찰할 수 있습니다. 당사의 응찰은 별도의 의사 표시 없이 진행할 수 있으며, 실제 응찰자의 유무와 관계없이 이루어질 수 있습니다. 다만, 당사 이외의 응찰자의 응찰금액이 내정가에 도달하지 못할 경우 당사의 응찰은 효력이 발생하지 않습니다.<br>
            7.3 응찰자가 제시한 응찰가격 중 내정가 이상의 응찰가격이 존재하지 않는 경우, 당사는 위탁자와 합의한 내정가에도 불구하고, 당사의 합리적인 재량에 따라 내정가 미만의 응찰가격 중 최고 응찰가격을 낙찰가로 결정할 수 있습니다.<br>
            7.4 공개응찰의 경우 응찰자는 당사 소정 양식의 응찰등록신청서를 작성한 후 응찰 팻말을 교부 받고 경매장에 참석하여 경매사의 호가에 응찰 팻말을 들어 응찰 의사 표시를 합니다.<br>
            7.5 응찰자의 사정에 의하여 공개 응찰에 참여하지 못할 경우에 응찰자는 당사 소정양식에 따라 서면 응찰을 할 수 있습니다. 서면응찰의 경우 당사 소정양식에 따라 작성된 서면이 당사에 도달된 때를 응찰 시점으로 간주하며 헤럴드아트데이 직원이 응찰자를 대리하여 경매장에서 응찰을 공개합니다. 서면응찰은 적어도 경매일 1일 이전에 헤럴드아트데이에 제출되어야 합니다. <br>
            7.6 응찰자의 사정에 의하여 공개 및 서면 응찰에 참여하지 못할 경우에는 응찰자는 당사가 정한 방법에 의하여 전화로 응찰 할 수 있으며, 전화 응찰은 녹음될 수 있습니다. 응찰자는 전화응찰을 함으로써 위 녹음에 동의한 것으로 간주합니다.<br>
            7.7 공개 응찰자, 전화 응찰자, 서면 응찰자가 동일한 가격으로 낙찰을 원하는 경우, 낙찰 우선순위는 서면, 공개, 전화 응찰자의 순서이며, 동일한 가격의 서면 응찰자가 중복될 경우에는 당사에 먼저 서면 응찰서를 헤럴드아트데이에 제출한 서면 응찰자에게 낙찰됩니다.<br>
            7.8 경매물품에 관하여, 경매약관에 따라 서면응찰 또는 전화응찰이 헤럴드아트데이에 수령된 경우, 헤럴드아트데이는 위 서면응찰 또는 전화응찰이 실시되도록 합리적인 노력을 기울입니다. 단, 위 응찰이 반영되지 않거나, 지연된 경우 또는 위 응찰과 관련한 헤럴드아트데이, 그 관리자, 직원, 피용인 그리고 대리인의 어떠한 오류나 누락에 대해서도 헤럴드아트데이는 그 사유를 불문하고 책임을 지지 않습니다.<br>
            7.9 내정가 이상(다만, 이 약관 제7.3조 따라, 당사는 예외적으로 내정가 미만의 가격으로 낙찰가를 결정할 수 있습니다)으로 응찰한 공개, 서면 및 전화 응찰자 중에서 최고 입찰가격을 제시한 응찰자에게 낙찰됩니다.<br>
            7.10 헤럴드아트데이 경매사는 아래와 같은 합리적인 재량권을 행사할 수 있는 권한을 갖습니다.<br>
            (ⅰ) 호가는 전적으로 경매사의 재량에 의하며 경매사는 경매진행시 호가 증가분을 미리 제시합니다. 응찰자는 구두로 원하는 금액을 제시할 수 있으나 경매사에 의해 받아들여지지 않을 수 있습니다.<br>
            (ⅱ) 경매사는 어떠한 응찰도 거절할 수 있습니다.<br>
            (ⅲ) 경매사는 경매 진행 방식에 대한 결정권을 갖습니다. <br>
            (ⅳ) 경매사는 경매 시작가를 결정할 권한이 있으며, 경매 시작가는 내정가 및 추정가의 구속을 받지 않습니다.<br>
            7.11 헤럴드아트데이는 절대적인 재량에 따라 다음의 사항을 실시할 수 있습니다.<br>
            (i) 예정된 경매일 이전 어느 때나 사전 공지없이 경매물품의 경매를 철회<br>
            (ⅱ) 사전 공지없이 둘 이상의 경매물품을 일괄하여 경매<br>
            (ⅲ) 경매기간동안 경매에 부쳐졌으나 판매되지 않은 경매물품의 경매를 같은 경매기간 중에 사전 공지없이 재개<br>
            (ⅳ) 응찰과 관련하여 어떠한 오류가 발생하거나 응찰의 유효성에 대하여 응찰자들 사이에 분쟁이 발생한 경우, 낙찰자를 결정, 응찰 절차를 진행, 경매를 취소 또는 경매물품을 재경매<br>
            (v) 작품의 진위여부 또는 경매의 유효성에 관하여 분쟁이 발생하거나 헤럴드아트데이의 단독 판단에 의할 때 분쟁이 발생할 소지가 있는 경우, 경매물품을 경매에 부치지 않거나 응찰 절차를 중단 또는 경매를 취소<br>
            7.12 위탁자는 자신이 출품한 경매물품에 직•간접적으로 응찰할 수 없습니다.<br>
            <br>
            8. 구매수수료<br><br>
            8.1 구매 수수료는 낙찰가의 12%(부가가치세 별도)를 적용하여 산출합니다. <br>
             (예시 1) 낙찰가가 5천만원일 경우<br>
                       1) 낙찰가 50,000,000 x 12% = 6,000,000원의 세전 구매 수수료와 부가가치세 600,000을 합산한 구매 수수료는 6,600,000원입니다.<br>
                       2) 구매대금은 50,000,000원(낙찰가)+6,6000,000원(구매 수수료)= 56,600,000원입니다.<br>
            8.2 구매수수료는 낙찰자가 헤럴드아트데이에 지급해야 하는 낙찰자비용을 제외한 <br>
            금액입니다.<br>
            <br>
            9. 구매대금의 지급<br>
            9.1 낙찰자는 낙찰일로부터 7일 이내(다만 낙찰가가 3억원 이상일 경우에는 낙찰일로부터 21일 이내, 이하 “납입기간”이라 한다)에 구매수수료를 포함한 구매대금을 완납해야 합니다. 기간계산에 있어서 경매당일은 산입하지 않습니다. 다만, 납입기간의 마지막 날이 영업일이 아닐 경우, 납입기간의 마지막 날로부터 가장 먼저 도래하는 영업일까지 납입기간이 연장되는 것으로 합니다. <br>
            9.2 낙찰자는 구매대금을 당사 은행계정에 송금하거나 현금, 자기앞수표 등으로 지급할 수 있으며, 신용 카드로는 결제할 수 없습니다. 다만, 구매수수료에 한하여 신용카드로 결제할 수 있습니다.<br>
            9.3	 낙찰자는 낙찰자로 결정된 후 당사와의 합의 하에 구매대금을 분할 납부할 수 있습니다. <br>
            9-4	 낙찰자와 헤럴드아트데이와 별도의 합의가 없는 이상 낙찰자가 구매대금을 제9.1조에서 정한 납입기간 내에 완납하지 않을 경우 당사는 아래 각 호 중 하나 이상의 조치를 취할 수 있는 권한을 갖습니다.  <br>
            9.4.1	납입기간 경과일부터 낙찰자가 구매대금을 모두 지급하는 날까지 납입 지연 일수에 대하여 1년을 365일로 보고 연 18%의 연체이자를 부과합니다.<br>
            9.4.2	낙찰자에게 구매대금 및 제1항의 연체이자를 당사가 정한 합리적인 기간내에 완납할 것을 최고하며, 낙찰자가 동 기간내에 구매대금 및 제1항의 연체이자를 완납하지 않을 경우에는 당사는 낙찰자가 구매의사가 없는 것으로 간주하고 경매물품의 낙찰을 취소할 수 있습니다. <br>
            9.4.3	당사는 제9.4.2호에 의해 낙찰이 취소된 경매물품을 재경매 및 다른 방법으로 처분(이하 “재처분”)할 수 있습니다. 이때 낙찰자는 i) 낙찰가보다 당사가 재처분한 가액이 낮을 경우, 낙찰가에서 재처분한 가액을 공제한 차액과, ii) 낙찰가에 대한 구매수수료보다 당사가 재처분한 가액에 대한 구매수수료가 낮을 경우, 낙찰가에 대한 구매수수료에서 재처분한 가액에 대한 구매수수료를 공제한 차액과, iii) 기타 재처분을 위하여 소요된 모든 비용을 지급할 의무가 있습니다.  <br>
            9.4.4	제9.4.2호에 의해 낙찰이 취소된 경우, 당사는 낙찰자를 상대로 당사에게 발생한 손해의 배상을 청구할 수 있으며, 또한 위탁자를 대신하여 낙찰자를 상대로 위탁자에게 발생한 손해배상을 청구할 수 있습니다. 또한, 이에 따른 모든 비용을 청구할 수 있습니다. <br>
            9.4.5	당사는 손해배상 청구나 구매대금의 회수를 위하여 법령이 허용하는 범위 내에서 낙찰자의 개인정보를 신용정보업자 등 제3자에게 제공할 수 있으며, 낙찰자는 이에 동의합니다.  <br>
            9.4.6	본 조에 의한 구매대금 지급의무를 위반한 낙찰자에 대하여는, 향후 경매에서 응찰을 제한하거나 응찰 전에 응찰보증금을 받을 수 있습니다.<br>
            9.4.7	당사가 낙찰자에 대한 금전지급채무를 부담하고 있는 경우에는 낙찰자가 당사에게 지급할 구매대금 등과 상계할 수 있습니다. <br>
            9.4.8	기타 헤럴드아트데이가 필요하거나 적절하다고 판단하는 조치를 취할 수 있습니다.<br>
            9.5	낙찰자는 원칙적으로 낙찰을 철회할 수 없습니다. 다만, 당사가 낙찰 철회에 동의한 경우에 한하여, 낙찰자는 낙찰철회의 의사표시와 함께 구매대금(구매수수료 포함)의 20%에 해당하는 해약금을 납부한 후 낙찰을 철회할 수 있습니다. 다만 낙찰을 철회한 회원은 향후 응찰 자격이 제한 될 수 있으며, 낙찰자의 낙찰철회로 인하여 당사에게 손해가 발생할 경우 당사는 위 해약금의 수령과는 별도로 낙찰자에게 손해배상을 청구할 수 있습니다.<br><br>
            10. 경매물품의 인도<br>
            10.1 헤럴드아트데이와 낙찰자간에 별도의 합의가 없다면, 헤럴드아트데이는 해당 경매물품의 낙찰자 또는 그의 권한 있는 대리인에게만 경매물품을 인도합니다. <br>
            10.2 낙찰자는 제9조에서 정한 납입기간 이내에 자신의 위험과 비용으로 자신이 매수한 경매물품을 직접 또는 대리인을 통해 인수합니다. 단, 다음 각호의 경우에는 헤럴드아트데이는 경매물품을 인도할 의무가 없습니다.<br>
            10.2.1 낙찰자가 헤럴드아트데이에 제9조에 따라 지급해야 하는 총구매대금을 완납하지 않은 경우<br>
            10.2.2 헤럴드아트데이가이 만족할 만한 수준의 인수인의 신원확인 증명 및 (권한 있는 대리인의 경우) 그 대리권의 증명이 없는 경우.<br>
            10.3	 헤럴드아트데이는은 낙찰자의 위험부담 하에 낙찰자가 낙찰받은 경매물품의 포장, 인도 및 운송보험의 가입을 주선할 수 있습니다. 헤럴드아트데이는 위 포장 및 인도 기간동안 발생한 어떠한 손해나 손실에 대해서도 책임을 지지 않습니다.<br>
            10.4	 헤럴드아트데이는은 낙찰자가 제9조에서 정한 납입기간내에 구매대금을 납입하고 경매물품을 인수하지 않은 경우, 매 경매물품마다 3만원/일에 상당한 보관비용을 낙찰자에게 부담시킬 수 있습니다.  단, 헤럴드아트데이와 낙찰자간에 별도의 합의가 있는 경우 그 합의에 의합니다.<br>
            10.5	 경매물품 인수 기일이 지난 물품의 보관에 있어 당사는 당사에게 고의 또는 중과실이 없는 한 해당 경매물품의 멸실 또는 손상에 대한 책임을 부담하지 않습니다. <br>
            <br>
            11. 경매물품의 위험과 소유권 이전 <br><br>
            경매물품과 관련된 위험과 책임(손상, 도난, 파손, 멸실 등)은 (i) 해당 경매물품의 낙찰일로부터 7일이 초과된 날 및 (ii) 낙찰자에 의한 위 경매물품의 인수일 중 먼저 도달하는 날에 낙찰자에게 이전됩니다. 본 항의 기간 계산에 있어 낙찰일 당일은 산입되지 않습니다. 그러나 낙찰자가 경매약관에 따라 헤럴드아트데이에 경매물품과 관련하여 부담하는 모든 대금을 헤럴드아트데이에 지급하지 않는 이상, 경매물품의 소유권은 낙찰자에게 이전되지 않습니다.<br>
            <br>
            12. 수출/입 허가증<br><br>
            경매물품의 해외 반출/반입에 필요한 면허 또는 허가 (이하 "허가"라 한다)를 취득해야 할 책임은 낙찰자에게 있습니다. 낙찰자가 서면으로 요청하는 경우, 헤럴드아트데이는 낙찰자의 비용과 위험부담 하에 낙찰자에게 필요한 허가 취득과 관련된 업무를 처리해 줄 수 있는 운송업체를 주선해 줄 수 있습니다. 그러나 이러한 주선과 관련하여 헤럴드아트데이는 어떠한 책임도 부담하지 않습니다. 경매물품에 대한 해외 반입/반출 허가 발급이 거부되거나 지연되었다고 하여, 낙찰자는 낙찰을 취소할 수 없으며 제9조에 따라 낙찰자가 헤럴드아트데이에 지급하여야 하는 총구매대금 지급을 거절하거나 지연할 수 없습니다.<br>
            <br>
            13. 경매 후 세일<br>
            13.1 위탁자가 경매물품을 위탁하면서 당사에 별도의 반대의사를 표시하지 않으면, 경매물품이 경매 출품 후 유찰될 경우에 당사가 경매 물품을 당사가 정하는 일정 기간 동안 보관하고, 이를 해당 경매일 이후에 다시 출품하는 것(이하 “경매 후 세일”)에 대하여 위탁자가 동의하는 것으로 봅니다. 단, 위탁자와 당사 사이에 별도의 합의가 없는 한, 경매 후 세일의 출품 여부는 당사가 합리적인 재량에 따라 결정하며, 당사는 경매물품을 경매 후 세일에 출품하는 것을 거절할 수 있습니다. <br>
            13.2 경매 후 세일 관련하여, 응찰자는 헤럴드아트데이가 서면으로 요구하는 보충서류를 헤럴드아트데이가 요구 시 정한 기간 이내에 헤럴드아트데이에 제공하여야 합니다. <br>
            13.3 경매 후 세일은 헤럴드아트데이가 위 응찰자의 응찰에 대한 수락을 통지하는 일자에 성립하며 헤럴드아트데이는 응찰자에게 경매물품, 경매약관과 유의사항에 따라 낙찰자가 헤럴드아트데이에 부담하는 낙찰가, 구매수수료 그리고 모든 낙찰자비용에 대한 사항을 포함하여 응찰자에게 통지하여야 합니다. 단, 헤럴드아트데이는 절대적인 재량에 의해, 관련 응찰자가 제6.2조에 의해 제공한 서류가 충분하지 않다고 판단하는 경우, 어떠한 응찰도 수락할 의무가 없습니다.<br>
            13.4 경매 후 세일에는 이 약관이 동일하게 적용됩니다.<br>

