            6. 응찰등록 <br>
            6.1 헤럴드아트데이 경매에 응찰하기 위해서는 이름과 주소가 등록되어져야 하며 대리응찰 하고자 하는 경우에는 대리 응찰자를 포함하여 위임자의 이름과 주소가 등록되어야 합니다. <br>
            6.2 처음 응찰하는 응찰자를 포함하여  회사에 등록되지 않은 응찰자의 경우에는 <br>
            정부에서 발행된 사진이 있는 신분증(주민등록증, 운전면허증) 혹은 여권을 제시하여 등록하여야 합니다.(사업자의 경우 사업자등록증을 제시) 당사는 별도의 보충서류가  필요하다고 판단하는 경우 그 서류를 요구할 수 있습니다.<br>
            6.3 응찰희망자는 경매당일 적어도 30분전에 경매장에서 응찰용 패들을 수령하여야 합니다. <br>
            6.4 응찰희망자는 전시기간 중에 서면/전화응찰 신청서를 작성하여 제출함으로써 응찰등록을 할 수 있습니다. <br>
            6.5 당사는 당사경매 참가 등록과 서면응찰 또는 전화응찰 신청을 처리하기 위하여 합리적인 노력을 기울입니다.  단, 응찰등록의 불이행, 지연 또는 응찰등록과 관련한 당사, 그 관리자, 직원, 피용인 또는 대리인의 어떠한 오류, 누락에 대해서도 당사는 그 사유를 불문하고 책임을 부담하지 않습니다.<br>
            6.6 당사는 별도의 설명없이 당사의 절대적 재량으로 모든 응찰등록을 거부할 수 있습니다.<br>
