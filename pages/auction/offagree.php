            5. 추정가<br><br>

            5.1 헤럴드아트데이는 경매도록, 인터넷 홈페이지 및 광고물에 각 경매물품에 대하여 낮은 추정가와 높은 추정가를 한국원화, 미국달러로 제공합니다. 제공된 추정가는 단지 응찰자의 편의를 위한 것이며, 경매물품의 실제가격이나 낙찰가를 의미하지 않으며 낙찰수수료를 포함하지 않습니다. 추정가는 공지 없이 각 경매물품의 경매 이전에 헤럴드아트데이의 절대적인 재량에 의해 변경될 수 있습니다. <br>
            5.2 경매도록, 인터넷 홈페이지 및 광고물에 미국달러로 표시된 추정가는 단지 응찰자의 편의를 위한 것이며, 모든 응찰과 낙찰대금, 위탁대금은 한국원화에 의합니다. 경매도록에 표시된 추정가와 관련하여 한국원화로 표시된 추정가를 미국달러로 환산한 경우, 해당되는 시점의 환율에 따라 금액에 차이가 있을 수 있습니다. 또한 이 환율은 경매도록의 인쇄 이전 가장 최근의 것을 기준으로 한 것이며, 그 이후 변경되었을 수 있습니다. 본 경매도록 등에 표시된 미국달러로 표시된 어떠한 금액도 한국원화 금액을 정확히 반영한다고 보장하지 않으며, 또한 위 금액이 특정한 환율로 계산된 경우의 원화금액을 반영한다는 것을 보장하지도 않습니다. 헤럴드아트데이는 한국원화로 표시된 추정가의 미국달러로의 환산과 관련하여 발생한 어떠한 오류나 누락에 대해서도, 그 사유를 불문하고 책임지지 않습니다. <br>
