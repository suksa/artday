            14. 제한보증<br>
            14.1 헤럴드아트데이는 경매도록에 해당 경매물품과 관련하여 굵게 인쇄된 표제사항(관련 경매기간동안 수정되는 사항 포함)에 대하여 위 경매물품의 낙찰일을 기준으로, 위 사항이 진정하고 정확하다는 것을 보증합니다. 이러한 표제사항 외 사항(보조 설명을 포함합니다) 또는 당사 임직원의 말 또는 글로 표현한 내용 등에 대하여는 보증을 하지 않습니다.  <br>
            14.2 당사의 보증 기간은 낙찰일로부터 3년 이내이며, 이 기간 중 제한 보증사항을 위반한 사항이 발견되어 낙찰자가 본 조에 정한 절차에 따라 서면으로 경매취소 요구를 할 경우, 당사는 본 조에 정한 사항의 준수 여부를 확인한 후 경매물품에 대한 경매를 취소하며, 낙찰자는 본 조에 정한 절차에 따라 구매대금을 반환 받게 됩니다. 당사의 보증책임은 경매물품에 대한 경매 취소 및 낙찰자에게 구매대금을 반환하는 것에 한정하며, 당사는 그 이외의 책임을 부담하지 않습니다. <br>
            14.3 낙찰자는 제2항의 경매취소 요구와 관련하여, 보증 기간 내에 경매물품에 대한 제한보증 사항이 중대하게 위반되었다는 점(이를 입증하기 위한 증거를 첨부)을 기재한 경매취소 요구 통지서(경매일자, 경매번호(Lot)를 기재)를 당사에 제출하여야 합니다.<br>
            14.4 당사는 경매취소 여부를 결정하기 전에, 낙찰자에게 당사와 낙찰자, 위탁자가 상호 인정하는 두 명의 해당 분야 전문가에게 문서에 의한 의견을 요구할 수 있습니다. <br>
            14.5 당사의 제2항에 의한 경매 취소시 낙찰자는 낙찰 받은 시점과 동일한 상태로 <br>
            경매물품을 당사에 인도하여야 합니다. <br>
